import numpy as np
import math

class Docs:
    def __init__(self, query, answers):
        self.query = set(query)
        self.answers = {k: set(v) for k, v in answers.items()}

        self.graph = {}
        self.I = {}
        self.R = {}
        self.P = {}

        self.alpha = 0.1
        self.lam = 0

    # Step 1: Compute P(Di | q)
    def compute_P(self):
        for key, value in self.answers.items():
            if len(self.query) == 0:
                self.P[key] = 0
            else:
                self.P[key] = len(value & self.query) / len(self.query)

    # Step 2: Compute Iw(D)
    def compute_I(self):
        all_sims = []

        # first pass → compute similarities
        pairwise_sims = {}

        for key, value in self.answers.items():
            pairwise_sims[key] = []

            for k, v in self.answers.items():
                if k != key:
                    if len(value | v) == 0:
                        sim = 0
                    else:
                        sim = len(value & v) / len(v)

                    pairwise_sims[key].append(sim)
                    all_sims.append(sim)



        # second pass → apply threshold
        for key, sims in pairwise_sims.items():
            wts = [sim for sim in sims]
            self.graph[key] = wts
            self.I[key] = sum(wts)

    # Step 3: Compute final rank
    def compute_R(self):
        # dynamic lambda
        k = len(self.answers)
        self.lam = 1 / (1 + math.log(k + 1))

        for key in self.answers:
            t_seconds=time[key].total_seconds()
            self.R[key] = self.P[key] * (1 + self.lam * self.I[key]) * 1/float(t_seconds) if t_seconds>0 else 0
    # Step 4: Full pipeline
    def solve(self):
        self.compute_P()
        self.compute_I()

        # normalize I
        max_I = max(self.I.values()) if self.I else 1
        for key in self.I:
            self.I[key] = self.I[key] / max_I if max_I > 0 else 0

        self.compute_R()

    # Step 5: Ranking
    def rank(self, top_k=5):
        return sorted(self.R.items(), key=lambda x: x[1], reverse=True)[:top_k]

